    private void selectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectButtonActionPerformed
        String input=jTextField2.getText();
        double height,width,thickness,x,y;
        try
        {
            height=Double.parseDouble(input);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"You must Enter positive height","Message",JOptionPane.WARNING_MESSAGE);
            return;
        }
        input=jTextField1.getText();
        try
        {
            thickness=Double.parseDouble(input);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"You must Enter positive thickness","Message",JOptionPane.WARNING_MESSAGE);
            return;
        }
        input=jTextField3.getText();
        try
        {
            x=Double.parseDouble(input);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"You must Enter positive position","Message",JOptionPane.WARNING_MESSAGE);
            return;
        }
        input=jTextField4.getText();
        try
        {
            y=Double.parseDouble(input);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"You must Enter positive position","Message",JOptionPane.WARNING_MESSAGE);
            return;
        }
        input=jTextField5.getText();
        try
        {
            width=Double.parseDouble(input);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"You must Enter positive width","Message",JOptionPane.WARNING_MESSAGE);
            return;
        }
        int x1=(int) Math.round(x);
         int y1=(int) Math.round(y);
        Point position = new Point(x1,y1);
       
        if(height<0||width<0||thickness<0||x<0||y<0)
         {
             JOptionPane.showMessageDialog(null,"You must Enter positive position","Message",JOptionPane.WARNING_MESSAGE);
             return;             
         }
        Map<String,Double> properties= new HashMap<>();
        properties.put("height", (double)height);
        properties.put("width",(double)width);
        properties.put("thickness",(double)thickness);
        Rectangle rectangle =new Rectangle(position,properties);
        painter.engine.addShape(rectangle);
        painter.repaint();
        dispose();
    }//GEN-LAST:event_selectButtonActionPerformed

